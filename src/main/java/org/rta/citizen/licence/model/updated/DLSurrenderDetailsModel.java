package org.rta.citizen.licence.model.updated;

public class DLSurrenderDetailsModel {

	private String comment;
	private String dlsReason;
	private String otherDlNumber;
	private String dlNumber;
	private String mergeStatus;

	public String getComment() {
		return comment;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}

	public String getDlsReason() {
		return dlsReason;
	}

	public void setDlsReason(String dlsReason) {
		this.dlsReason = dlsReason;
	}

	public String getOtherDlNumber() {
		return otherDlNumber;
	}

	public void setOtherDlNumber(String otherDlNumber) {
		this.otherDlNumber = otherDlNumber;
	}

	public String getDlNumber() {
		return dlNumber;
	}

	public void setDlNumber(String dlNumber) {
		this.dlNumber = dlNumber;
	}

	public String getMergeStatus() {
		return mergeStatus;
	}

	public void setMergeStatus(String mergeStatus) {
		this.mergeStatus = mergeStatus;
	}

}
